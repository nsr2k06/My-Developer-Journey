#include <iostream>
using namespace std;
int main(){

    //ARITHMETIC OPERATORS (+ - * / %)
    
    //INT FOR INTEGERS ONLY
    int x=20;
    int y=5;
    cout <<x+y<<endl;
    cout <<x-y<<endl;
    cout <<x*y<<endl;
    cout <<x/y<<endl;
    cout <<x%y<<endl;

    //FLOAT FOR REAL NO.
    float m=50.0;
    float n=2.5;
    cout <<m+n<<endl;
    cout <<m-n<<endl;
    cout <<m*n<<endl;
    cout <<m/n<<endl;

    return 0;
}